<p align="left" style="margin-bottom: 0in; font-weight: normal"><font face="Calibri, sans-serif">	a)
Entgegennahme der Berichte des Vorstandes,</font></p>
<p align="left" style="margin-bottom: 0in; font-weight: normal"><font face="Calibri, sans-serif">	b)
Entlastung und Wahl des Vorstandes,</font></p>
<p align="left" style="margin-bottom: 0in; font-weight: normal"><font face="Calibri, sans-serif">	c)
Festsetzung von Aufnahmegebühren, Beiträgen, Umlagen und deren
Fälligkeit</font></p>
<p align="left" style="margin-bottom: 0in; font-weight: normal"><font face="Calibri, sans-serif">	d)
Genehmigung des Haushaltsplanes,</font></p>
<p align="left" style="margin-bottom: 0in; font-weight: normal"><font face="Calibri, sans-serif">	e)
Satzungsänderungen,</font></p>
<p align="left" style="margin-bottom: 0in; font-weight: normal"><font face="Calibri, sans-serif">	f)
Beschlussfassung über Anträge,</font></p>
<p align="left" style="margin-bottom: 0in; font-weight: normal"><font face="Calibri, sans-serif">	g)
Entscheidung über die Berufung gegen den ablehnenden Entscheid des
Vorstandes nach 	§ 3, Abs. 2 und Abs. 4</font></p>
<p align="left" style="margin-bottom: 0in; font-weight: normal"><font face="Calibri, sans-serif">	h)
Auflösung des Vereins.</font></p>
<p align="left" style="margin-bottom: 0in; font-weight: normal"><br/>

</p>
<ol start="2">
	<li><p align="left" style="margin-bottom: 0in; font-weight: normal"><font face="Calibri, sans-serif">Eine
	ordentliche Mitgliederversammlung (Jahreshauptversammlung) findet
	mindestens einmal jährlich statt. Sie sollte grundsätzlich im 1.
	Quartal des Kalenderjahres stattfinden.</font></p></li>
	<p align="left" style="margin-bottom: 0in; font-weight: normal"></p>
	<li><p align="left" style="margin-bottom: 0in; font-weight: normal"><font face="Calibri, sans-serif">Eine
	außerordentliche Mitgliederversammlung ist innerhalb einer Frist
	von mindestens zwei Wochen mit entsprechender schriftlicher
	Tagesordnung einzuberufen, wenn es</font></p></li>
</ol>
<p align="left" style="margin-bottom: 0in; font-weight: normal"><font face="Calibri, sans-serif">	a)
der Vorstand beschließt oder</font></p>
<p align="left" style="margin-bottom: 0in; font-weight: normal"><font face="Calibri, sans-serif">	b)
25% der erwachsenen Mitglieder beantragen</font></p>
<p align="left" style="margin-bottom: 0in; font-weight: normal"><br/>

</p>
<ol start="4">
	<li><p align="left" style="margin-bottom: 0in; font-weight: normal"><font face="Calibri, sans-serif">Die
	Einberufung von Mitgliederversammlungen erfolgt durch den Vorstand
	mittels Einladung per E-mail. Zwischen dem Tag der Einladung und dem
	Termin der Versammlung muss eine Frist von mindestens zwei Wochen
	und höchstens sechs Wochen liegen. Mit der Einberufung der
	Mitgliederversammlung ist die Tagesordnung mitzuteilen. Anträge auf
	Satzungsänderungen müssen bei der Bekanntgabe der Tagesordnung
	wörtlich mitgeteilt werden.</font></p></li>
	<p align="left" style="margin-bottom: 0in; font-weight: normal"></p>
</ol>
<ol start="5">
	<li><p align="left" style="margin-bottom: 0in; font-weight: normal"><font face="Calibri, sans-serif">Die
	Mitgliederversammlung ist unabhängig von der Zahl der erschienen
	Mitglieder beschlussfähig. Bei Beschlüssen und Wahlen entscheidet
	die einfache Mehrheit der abgegebenen gültigen Stimmen.
	Stimmengleichheit bedeutet Ablehnung. Satzungsänderungen erfordern
	eine Zweidrittelmehrheit. Bei Wahlen muss eine geheime Abstimmung
	erfolgen, wenn diese von mindestens drei der stimmberechtigten
	Anwesenden beantragt wird.</font></p></li>
	<p align="left" style="margin-bottom: 0in; font-weight: normal"></p>
	<li><p align="left" style="margin-bottom: 0in; font-weight: normal"><font face="Calibri, sans-serif">Anträge
	können gestellt werden von Mitgliedern und vom Vorstand.</font></p>
	<p align="left" style="margin-bottom: 0in; font-weight: normal"></p></li>
	<li><p align="left" style="margin-bottom: 0in; font-weight: normal"><font face="Calibri, sans-serif">Anträge
	auf Satzungsänderungen müssen vier Wochen vor der
	Mitgliederversammlung schriftlich beim Vorsitzenden des Vereins
	eingegangen sein.</font></p></li>
	<p align="left" style="margin-bottom: 0in; font-weight: normal"></p>
	<li><p align="left" style="margin-bottom: 0in; font-weight: normal"><font face="Calibri, sans-serif">Über
	die Mitgliederversammlung ist ein Ergebnisprotokoll zu fertigen, das
	vom Versammlungsleiter und dem Protokollführer unterzeichnet werden
	muss. In dieses Protokoll sind insbesondere die Beschlüsse der
	Mitgliederversammlung aufzunehmen. Protokolle gelten als genehmigt,
	wenn binnen 6 Wochen nach Absendung kein schriftlicher Einspruch
	eingelegt wird.</font></p></li>
	<p align="left" style="margin-bottom: 0in; font-weight: normal"></p>
</ol>
